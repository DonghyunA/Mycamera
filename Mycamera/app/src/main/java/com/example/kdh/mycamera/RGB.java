public class RGB {

    int r;
    int g;
    int b;

    RGB(int R,int G, int B){
        this.r=R;
        this.g=G;
        this.b=B;
    }
    public int getR(){return r;}
    public int getG(){return g;}
    public int getB(){return b;}
}
